import pandas as pd

# Создание DataFrame с тестовыми данными
data = {
    'Ученик': ['Ученик1', 'Ученик2', 'Ученик3', 'Ученик4', 'Ученик5',
               'Ученик6', 'Ученик7', 'Ученик8', 'Ученик9', 'Ученик10'],
    'Математика': [78, 85, 92, 88, 76, 95, 89, 73, 84, 91],
    'Физика': [82, 79, 91, 87, 75, 88, 92, 80, 85, 90],
    'Химия': [85, 78, 89, 92, 77, 86, 93, 79, 81, 88],
    'Биология': [80, 83, 88, 86, 90, 92, 85, 78, 87, 84],
    'История': [77, 81, 85, 89, 82, 87, 90, 79, 83, 86]
}

df = pd.DataFrame(data)
print(df.head())

# Средние оценки по каждому предмету
average_scores = df.mean(numeric_only=True)
print("\nСредние оценки по предметам:")
print(average_scores)

# Медианные оценки по каждому предмету
median_scores = df.median(numeric_only=True)
print("\nМедианные оценки по предметам:")
print(median_scores)

# Квартильные значения для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math

print("\nQ1 для Математики:", Q1_math)
print("Q3 для Математики:", Q3_math)
print("IQR для Математики:", IQR_math)

# Стандартное отклонение по каждому предмету
std_deviation = df.std(numeric_only=True)
print("\nСтандартное отклонение по предметам:")
print(std_deviation)